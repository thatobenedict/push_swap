#include <unistd.h>

void	ft_puttab_fd(char **s, int fd)
{
	int		i;
	int		j;

	if (!s)
		return ;
	i = 0;
	while (s[i] != 0)
	{
		j = 0;
		while (s[i][j] != '\0')
		{
			write(fd, &s[i][j], 1);
			j++;
		}
		i++;
		write(fd, "\n", 1);
	}
}
