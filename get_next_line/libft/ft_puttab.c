#include <unistd.h>

void	ft_puttab(char **s)
{
	int		i;
	int		j;

	if (!s)
		return ;
	i = 0;
	while (s[i] != 0)
	{
		j = 0;
		while (s[i][j] != '\0')
		{
			write(1, &s[i][j], 1);
			j++;
		}
		i++;
		write(1, "\n", 1);
	}
}
